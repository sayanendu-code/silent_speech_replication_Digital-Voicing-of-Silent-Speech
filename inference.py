    parser.add_argument('--transducer_ckpt', type=str,
                        default='./checkpoints/best_transducer.pt')
    parser.add_argument('--wavenet_ckpt', type=str,
                        default='./checkpoints/wavenet/best_wavenet.pt')
    parser.add_argument('--normalizer_path', type=str,
                        default='./data/processed/processed_data.pkl')
    parser.add_argument('--output_dir', type=str, default='./outputs')
    parser.add_argument('--session_id', type=int, default=0)
    parser.add_argument('--gpu', type=int, default=0)
    args = parser.parse_args()
    main(args)
